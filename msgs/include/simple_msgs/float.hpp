#ifndef SIMPLE_MSGS_FLOAT_H
#define SIMPLE_MSGS_FLOAT_H

#include "generated/float_generated.h"
#include "numeric_type.hpp"

// Specialized functions for NumericType<float>.

namespace simple_msgs {

//! Typedef for Float messages.
using Float = NumericType<float>;

/**
 * @brief Construct a Float message using a raw memory coming from network.
 */
template <>
inline NumericType<float>::NumericType(const void* data) : data_{GetFloatFbs(data)->data()} {}

/**
 * @brief Copy assignment operator that uses raw memory coming from the network.
 */
template <>
NumericType<float>& NumericType<float>::operator=(std::shared_ptr<void*> data) {
  data_.store(GetFloatFbs(*data)->data());
  return *this;
}

/**
 * @brief Builds and returns the buffer accordingly to the value currently stored.
 */
template <>
std::shared_ptr<flatbuffers::DetachedBuffer> NumericType<float>::getBufferData() const {
  flatbuffers::FlatBufferBuilder builder{1024};
  FloatFbsBuilder tmp_builder{builder};
  tmp_builder.add_data(data_.load());
  FinishFloatFbsBuffer(builder, tmp_builder.Finish());
  return std::make_shared<flatbuffers::DetachedBuffer>(builder.Release());
}

/**
 * @brief Returns an identifier of the message type generated by the flatbuffers.
 */
template <>
inline std::string NumericType<float>::getTopic() {
  return FloatFbsIdentifier();
}
}  // Namespace simple_msgs.

#endif  // SIMPLE_MSGS_FLOAT_H
